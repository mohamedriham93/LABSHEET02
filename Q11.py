input = int(input("Enter the number of the day in a week: "))

if input == 1:
    print ("Monday")

elif input == 2:
    print ("Tuesday")

elif input == 3:
    print ("Wednesday")

elif input == 4:
    print ("Thursday")

elif input == 5:
    print ("Friday")

elif input == 6:
    print ("Saturdau")

elif input == 7:
    print ("Sunday")

else :
    print ("The number you entered is not exist")
